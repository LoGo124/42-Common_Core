#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*res;

	res = NULL;
	while (*s)
	{
		if ((char)c == *s)
			res = s;
		s++;
	}
	if (*s == (char)c)
		res = s;
	return (res);
}
